# ==========================================
# Demand Prediction using XGBoost
# ==========================================

import pandas as pd
import numpy as np
import joblib

from sklearn.model_selection import train_test_split
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

from xgboost import XGBRegressor

# ------------------------------------------
# Load Dataset
# ------------------------------------------
df = pd.read_csv("/content/cleaned_shipment_demand_dataset.csv")

print("Dataset Loaded Successfully!")

# ------------------------------------------
# Convert Date Column
# ------------------------------------------
df["Date"] = pd.to_datetime(df["Date"])

df["Month"] = df["Date"].dt.month
df["Day_Num"] = df["Date"].dt.day

# Drop original Date column
df.drop("Date", axis=1, inplace=True)

# ------------------------------------------
# Convert Categorical Columns
# ------------------------------------------
df = pd.get_dummies(df, columns=["Day_of_Week", "Holiday"], drop_first=True)

# ------------------------------------------
# Features & Target
# ------------------------------------------
X = df.drop("Shipment_Volume", axis=1)
y = df["Shipment_Volume"]

# ------------------------------------------
# Split Dataset
# ------------------------------------------
X_train, X_test, y_train, y_test = train_test_split(
    X,
    y,
    test_size=0.20,
    random_state=42
)

# ------------------------------------------
# Create XGBoost Model
# ------------------------------------------
model = XGBRegressor(
    n_estimators=200,
    learning_rate=0.05,
    max_depth=6,
    random_state=42
)

# ------------------------------------------
# Train Model
# ------------------------------------------
model.fit(X_train, y_train)

# ------------------------------------------
# Prediction
# ------------------------------------------
y_pred = model.predict(X_test)

# ------------------------------------------
# Evaluation
# ------------------------------------------
mae = mean_absolute_error(y_test, y_pred)
mse = mean_squared_error(y_test, y_pred)
rmse = np.sqrt(mse)
r2 = r2_score(y_test, y_pred)

print("\n========== XGBoost Performance ==========")
print("MAE :", round(mae, 2))
print("MSE :", round(mse, 2))
print("RMSE:", round(rmse, 2))
print("R2 Score :", round(r2, 4))
print("Accuracy :", round(r2 * 100, 2), "%")

# ------------------------------------------
# Save Model
# ------------------------------------------
joblib.dump(model, "xgboost_model.pkl")

print("\nModel Saved Successfully!")