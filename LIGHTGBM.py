import pandas as pd
import numpy as np
import lightgbm as lgb
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
import matplotlib.pyplot as plt

from google.colab import files
uploaded = files.upload()

df = pd.read_csv(r'/content/cleaned_shipment_demand_dataset (1).csv')
df['Date'] = pd.to_datetime(df['Date'])
df = df.sort_values('Date').reset_index(drop=True)
df.head()

le_day = LabelEncoder()
le_holiday = LabelEncoder()

df['Day_of_Week_enc'] = le_day.fit_transform(df['Day_of_Week'])
df['Holiday_enc'] = le_holiday.fit_transform(df['Holiday'])

df.head()

features = ['Year', 'Week', 'Day', 'Day_of_Week_enc', 'Quarter', 'Holiday_enc',
            'Previous_Day_Demand', 'Previous_Week_Demand',
            'Rolling_7Day_Avg', 'Rolling_30Day_Avg',
            'Total_Weight_lbs', 'Total_Revenue', 'Load_Count']

target = 'Shipment_Volume'

X = df[features]
y = df[target]

split_idx = int(len(df) * 0.8)

X_train, X_test = X.iloc[:split_idx], X.iloc[split_idx:]
y_train, y_test = y.iloc[:split_idx], y.iloc[split_idx:]

print("Train size:", X_train.shape[0])
print("Test size:", X_test.shape[0])

train_data = lgb.Dataset(X_train, label=y_train)
test_data = lgb.Dataset(X_test, label=y_test, reference=train_data)

params = {
    'objective': 'regression',
    'metric': 'rmse',
    'boosting_type': 'gbdt',
    'num_leaves': 31,
    'learning_rate': 0.05,
    'feature_fraction': 0.9,
    'verbose': -1
}

lgb_model = lgb.train(
    params,
    train_data,
    num_boost_round=500,
    valid_sets=[train_data, test_data],
    valid_names=['train', 'test'],
    callbacks=[lgb.early_stopping(stopping_rounds=30), lgb.log_evaluation(50)]
)

y_pred = lgb_model.predict(X_test, num_iteration=lgb_model.best_iteration)

mae = mean_absolute_error(y_test, y_pred)
rmse = np.sqrt(mean_squared_error(y_test, y_pred))
r2 = r2_score(y_test, y_pred)

print(f"MAE  : {mae:.2f}")
print(f"RMSE : {rmse:.2f}")
print(f"R2   : {r2:.4f}")

plt.figure(figsize=(12,5))
plt.plot(y_test.values, label='Actual', marker='o', markersize=3)
plt.plot(y_pred, label='Predicted', marker='x', markersize=3)
plt.title('LightGBM: Actual vs Predicted Shipment Volume')
plt.xlabel('Test Sample Index')
plt.ylabel('Shipment Volume')
plt.legend()
plt.grid(alpha=0.3)
plt.show()

importance = pd.DataFrame({
    'Feature': features,
    'Importance': lgb_model.feature_importance(importance_type='gain')
}).sort_values('Importance', ascending=False)

plt.figure(figsize=(8,6))
plt.barh(importance['Feature'], importance['Importance'])
plt.gca().invert_yaxis()
plt.title('LightGBM Feature Importance')
plt.xlabel('Gain')
plt.show()

