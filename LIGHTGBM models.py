import pandas as pd
import numpy as np
import lightgbm as lgb
from sklearn.metrics import mean_squared_error, mean_absolute_error, r2_score

# ==========================================
# 1. DATA LOAD & SORTING
# ==========================================
# Path-ah unga environment-ku yethapadi check panni vachikonga
file_path = r'/content/preprocessed_demand_data.csv'
df = pd.read_csv(file_path)

# Date column-ah datetime format-ku maathi, chronological order-la sort pannrom
df['Date'] = pd.to_datetime(df['Date'])
df = df.sort_values('Date').reset_index(drop=True)

# ==========================================
# 2. FEATURE ENGINEERING & SELECTION
# ==========================================
target = 'OrderCount'

# Data Leakage thaduka 'NumberOfPieces' and 'TotalRevenue'-ah thookiyachu!
features = [
    'Holiday_Indicator_Encoded', 'Festival_Name_Encoded', 'Season_Encoded',
    'Year', 'Month', 'Day', 'DayOfWeek', 'WeekOfYear', 'IsWeekend', 'Quarter',
    'OrderCount_lag_1', 'OrderCount_lag_7', 'OrderCount_lag_14',
    'OrderCount_rolling_mean_7', 'OrderCount_rolling_std_7'
]

categorical_features = ['Hub', 'Region']

# FIX: String columns-ah LightGBM purijika 'category' data type-ku maathrom
for col in categorical_features:
    df[col] = df[col].astype('category')

# Total features list combine pannrom
all_features = features + categorical_features

X = df[all_features]
y = df[target]

# ==========================================
# 3. TIME-SERIES TRAIN & TEST SPLIT
# ==========================================
# Mudhal 80% data training-kum, last 20% data testing-kum use aagum
split_idx = int(len(df) * 0.8)

X_train, X_test = X.iloc[:split_idx], X.iloc[split_idx:]
y_train, y_test = y.iloc[:split_idx], y.iloc[split_idx:]

# ==========================================
# 4. LIGHTGBM DATASET PREPARATION
# ==========================================
train_data = lgb.Dataset(X_train, label=y_train, categorical_feature=categorical_features)
test_data = lgb.Dataset(X_test, label=y_test, reference=train_data, categorical_feature=categorical_features)

# ==========================================
# 5. MODEL HYPERPARAMETERS
# ==========================================
params = {
    'objective': 'regression',
    'metric': 'rmse',
    'boosting_type': 'gbdt',
    'learning_rate': 0.05,
    'num_leaves': 31,
    'max_depth': -1,
    'feature_fraction': 0.8,
    'verbose': -1,
    'random_state': 42
}

# ==========================================
# 6. MODEL TRAINING
# ==========================================
print("Training LightGBM model...")
model = lgb.train(
    params,
    train_data,
    num_boost_round=1000,
    valid_sets=[train_data, test_data],
    callbacks=[lgb.early_stopping(stopping_rounds=50), lgb.log_evaluation(100)]
)

# ==========================================
# 7. PREDICTION & EVALUATION
# ==========================================
y_pred = model.predict(X_test, num_iteration=model.best_iteration)

rmse = np.sqrt(mean_squared_error(y_test, y_pred))
mae = mean_absolute_error(y_test, y_pred)
r2 = r2_score(y_test, y_pred)

print("\n--- Model Evaluation ---")
print(f"RMSE: {rmse:.4f}")
print(f"MAE: {mae:.4f}")
print(f"R2 Score: {r2:.4f}")

# ==========================================
# 8. FEATURE IMPORTANCE
# ==========================================
importance = pd.DataFrame({
    'Feature': all_features,
    'Importance': model.feature_importance(importance_type='gain')
}).sort_values(by='Importance', ascending=False)

print("\n--- Top 5 Important Features ---")
print(importance.head(5))